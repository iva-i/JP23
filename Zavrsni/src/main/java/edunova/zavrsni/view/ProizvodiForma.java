    private void ucitaj() {
        
        DefaultListModel<Proizvod> m = new DefaultListModel<>();

        m.addAll(obrada.getPodaci());

        lstProizvodi.setModel(m);
        
    }

    private void postaviVrijednostiNaEntitet() {
        
        var entitet = obrada.getEntitet();
        
        entitet.setNaziv(txtNazivProizvoda.getText());
        
        try {
            entitet.setProizvodac(txtProizvodac.getText());
        } catch (Exception e) {
            entitet.setProizvodac("");
        }
        
        try {
            entitet.setOpis(txtOpis.getText());
        } catch (Exception e) {
            entitet.setOpis("");
        }

        try {
            entitet.setCijena(new BigDecimal(txtCijena.getText()));
        } catch (Exception e) {
            entitet.setCijena(BigDecimal.ZERO);
        }

        try {
            entitet.setKolicinaNaStanju(new BigDecimal(txtKolicinaNaStanju.getText()));
        } catch (Exception e) {
            entitet.setKolicinaNaStanju(BigDecimal.ZERO);
        }
        
    }

    private void pocisti() {
        
        txtNazivProizvoda.setText("");
        txtProizvodac.setText("");
        txtOpis.setText("");
        txtCijena.setText("");
        txtKolicinaNaStanju.setText("");
        
    }
