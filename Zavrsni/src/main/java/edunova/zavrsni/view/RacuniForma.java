    private void ucitajRacune() {
        
        DefaultListModel<Racun> m = new DefaultListModel<>();
        m.addAll(obradaRacun.getPodaci());
        lstPopisRacuna.setModel(m);
    }

    private void ucitajProizvodeRacuna() {
        
        List<Stavka> stavke = lstPopisRacuna.getSelectedValue().getStavke();
        DefaultListModel<Stavka> m = new DefaultListModel<>();
        m.addAll(stavke);
        lstStavkeOdabranogRacuna.setModel(m);  
    }

    private void prikaziCijenu() {
        
        List<Stavka> stavke = lstPopisRacuna.getSelectedValue().getStavke();
                
        double cj = 0;
        double cijenaProizvoda = 0;
        
        for(int i = 0; i < stavke.size(); i++){
            cijenaProizvoda = stavke.get(i).getKolicina().doubleValue() *  
                    stavke.get(i).getProizvod().getCijena().doubleValue();
            cj += cijenaProizvoda;
        }
        
        double rabat = lstPopisRacuna.getSelectedValue().getRabat().doubleValue();
        
        if(rabat != 0){
            rabat = 100 - rabat;
            rabat /= 100;
            cj *= rabat;
        }
        
        String uk = String.format("%.2f",cj);
        
        txtUkupno.setText(uk);
    }

    private void prikaziRabat() {        
        String rabat = String.valueOf(lstPopisRacuna.getSelectedValue().getRabat().doubleValue());
        txtRabat.setText(rabat);        
   }
