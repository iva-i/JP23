package Zadatak2v2;

public abstract class Porodica extends Carstvo{

	private Naziv nazivPorodica;
	private String karakteristike;
	
	
	public Naziv getNazivPorodica() {
		return nazivPorodica;
	}
	public void setNazivPorodica(Naziv nazivPorodica) {
		this.nazivPorodica = nazivPorodica;
	}
	public String getKarakteristike() {
		return karakteristike;
	}
	public void setKarakteristike(String karakteristike) {
		this.karakteristike = karakteristike;
	}
	
	
	
}
