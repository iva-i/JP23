package Zadatak2v2;

public abstract class Carstvo {

	private Naziv nazivCarstvo;
	private String opis;
	
	
	public Naziv getNazivCarstvo() {
		return nazivCarstvo;
	}
	public void setNazivCarstvo(Naziv nazivCarstvo) {
		this.nazivCarstvo = nazivCarstvo;
	}
	public String getOpis() {
		return opis;
	}
	public void setOpis(String opis) {
		this.opis = opis;
	}
	
	
}
